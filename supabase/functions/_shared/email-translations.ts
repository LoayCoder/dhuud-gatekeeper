// Helper function to get translations for a specific language
export function getTranslations<T>(translations: Record<SupportedLanguage, T>, lang: string): T {
  const validLang = (lang as SupportedLanguage) in translations ? (lang as SupportedLanguage) : 'en';
  return translations[validLang];
}

// Helper function to replace variables in a string
export function replaceVariables(text: string, variables: Record<string, string | number | undefined>): string {
  let result = text;
  for (const [key, value] of Object.entries(variables)) {
    result = result.replace(new RegExp(`\\{${key}\\}`, 'g'), String(value ?? ''));
  }
  return result;
}
